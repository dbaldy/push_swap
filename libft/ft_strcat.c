#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	i[2];

	i[0] = 0;
	while (s1[i[0]] != 0)
		i[0]++;
	i[1] = 0;
	while (s2[i[1]] != '\0')
	{
		s1[i[0] + i[1]] = s2[i[1]];
		i[1]++;
	}
	s1[i[0] + i[1]] = '\0';
	return (s1);
}
