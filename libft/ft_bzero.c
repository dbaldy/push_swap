#include "libft.h"

void	*ft_bzero(void *var, size_t j)
{
	size_t			k;
	unsigned char	*cpy;

	cpy = var;
	k = 0;
	while (k < j)
	{
		cpy[k] = '\0';
		k++;
	}
	return (var);
}
