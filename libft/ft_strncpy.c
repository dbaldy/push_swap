#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	size_t		k;

	k = 0;
	while (src[k] != '\0' && k < n)
	{
		dst[k] = src[k];
		k++;
	}
	if (k < n)
	{
		while (k < n)
		{
			dst[k] = '\0';
			k++;
		}
	}
	return (dst);
}
