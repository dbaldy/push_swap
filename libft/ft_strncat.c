#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i[2];

	i[0] = 0;
	while (s1[i[0]] != 0)
		i[0]++;
	i[1] = 0;
	while (s2[i[1]] != '\0' && i[1] < n)
	{
		s1[i[0] + i[1]] = s2[i[1]];
		i[1]++;
	}
	s1[i[0] + i[1]] = '\0';
	return (s1);
}
