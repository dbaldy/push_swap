#include "libft.h"

char	*ft_strnstr(const char *s1, const char *s2, size_t n)
{
	size_t		i[3];

	i[0] = 0;
	while (s1[i[0]] != '\0')
	{
		i[2] = 0;
		i[1] = 0;
		while (s2[i[1]] != '\0')
		{
			if (s1[i[0] + i[1]] == s2[i[1]] && i[0] + i[1] < n)
				i[2]++;
			i[1]++;
		}
		if (i[2] == i[1])
			return (&((char *)s1)[i[0]]);
		i[0]++;
	}
	return (NULL);
}
