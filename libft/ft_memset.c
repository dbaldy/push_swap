#include "libft.h"

void	*ft_memset(void *var, int i, size_t j)
{
	size_t			k;
	unsigned char	*cpy;

	cpy = var;
	k = 0;
	while (k < j)
	{
		cpy[k] = i;
		k++;
	}
	return (var);
}
