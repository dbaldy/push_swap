#include "libft.h"

int		ft_find_next_prime(int nb)
{
	int i[2];

	if (nb <= 2)
		return (2);
	else
	{
		i[1] = 1;
		while (i[1] != 0)
		{
			i[1] = 0;
			i[0] = 2;
			while (i[0] <= nb / (i[0] - 1))
			{
				if (nb % i[0] == 0)
					i[1] = 1;
				i[0]++;
			}
			nb++;
		}
		return (nb - 1);
	}
}
