#include "ft_printf.h"

int		ft_atoi(const char *str)
{
	int i[3];

	i[0] = 0;
	i[1] = 1;
	i[2] = 0;
	if (ft_strcmp(str, "-2147483648") == 0)
		return (-2147483648);
	while ((unsigned char)str[i[2]] < 33)
		i[2]++;
	if (str[i[2]] == '-')
	{
		i[1] = -1;
		i[2]++;
	}
	else if (str[i[2]] == '+')
		i[2]++;
	while (str[i[2]] == '0')
		i[2]++;
	while (str[i[2]] != '\0' && str[i[2]] <= '9' && str[i[2]] >= '0')
	{
		i[0] = i[0] * 10 + str[i[2]] - '0';
		i[2]++;
	}
	return (i[0] * i[1]);
}
